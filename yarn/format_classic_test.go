func TestYarnClassic_TransitiveDeps(t *testing.T) {
	reg := newMockRegistry()
	reg.addVersion("A", "1.0.0", baseTime, map[string]string{"B": "^2.0.0"})
	reg.addVersion("B", "2.1.0", baseTime, map[string]string{"C": "^1.0.0"})
	reg.addVersion("C", "1.3.0", baseTime, nil)

	project := &ecosystem.ProjectSpec{
		Name:    "test-project",
		Version: "1.0.0",
		Dependencies: []ecosystem.DeclaredDep{
			{Name: "A", Constraint: "^1.0.0", Type: ecosystem.DepRegular},
		},
	}

	resolver := NewResolver()
	result, err := resolver.ResolveForLockfile(context.Background(), project, reg, ecosystem.ResolveOptions{})
	if err != nil {
		t.Fatalf("resolve failed: %v", err)
	}

	formatter := NewYarnClassicFormatter()
	output, err := formatter.FormatFromResult(result, project)
	if err != nil {
		t.Fatalf("format failed: %v", err)
	}

	lockfile := string(output)
	t.Logf("Generated lockfile:\n%s", lockfile)

	// All three packages should appear.
	if !strings.Contains(lockfile, "A@^1.0.0:") {
		t.Error("missing A entry")
	}
	if !strings.Contains(lockfile, "B@^2.0.0:") {
		t.Error("missing B entry")
	}
	if !strings.Contains(lockfile, "C@^1.0.0:") {
		t.Error("missing C entry")
	}

	// Verify resolved versions.
	if !strings.Contains(lockfile, `version "1.0.0"`) {
		t.Error("missing A version 1.0.0")
	}
	if !strings.Contains(lockfile, `version "2.1.0"`) {
		t.Error("missing B version 2.1.0")
	}
	if !strings.Contains(lockfile, `version "1.3.0"`) {
		t.Error("missing C version 1.3.0")
	}
}
